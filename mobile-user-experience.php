                        <h4>Menus and Navigation: Keep It Simple</h4>
                        <p class="style2">Traditional desktop websites display a prominent menu bar at the top of the page. On a mobile this eats up precious screen space. To resolve this, make the menu a drop down accordion or icon on the top left or right of the mobile screen.

Another desktop habit that doesn't work well on mobiles is multi level menus with sub menus that show on hover. On a mobile device, you want to keep things accessible. If the user has to tap through 4 levels of menus to find something, chances are high they'll leave after the 2nd tap. Avoid multi level menus on mobile sites as much as possible.</p>
                        
                        </div>
                        <div class="col-sm-4  services-full-width-text">
                        <h4>Create Fluid Layouts</h4>
                        <p class="style2">Many mobile devices means many different dimensions. However tempting it may be, don't just design for a 320 pixel width. Like it or not  176, 240, 320, 360, ~480-600 (landscape) are also common device widths. Keeping your layout flexible and fluid ensures it displays properly on different screen sizes. You don't want the site to work just on devices that match your fixed break points but look weird on everything else. Here's a primer to fluid layouts and one on getting fluid layouts working in responsive designs.</p>
                        
