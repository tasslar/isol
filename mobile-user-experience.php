                        <h4>Keep Forms Minimal</h4>
                        <p class="style2">Small touch screens and yet smaller virtual keyboards with keys that are barely 5mm x 5mm in size do not make for a happy typing experience. Keep forms simple and small. If you need to, keep a separate form for mobile users, with the least number of fields required to get the data you need. As far as possible, pre-fill fields with defaults. Use auto-fill for commonly used fields.

For example, use visual calendars instead of making users type the date. For forms that are longer than a single screen, it's good to show users a progress bar indicating how far they've come, and more importantly, how close they are to finishing. Here's some good guidelines from Google to create good forms..</p>
