<p>Selenium is not just a single tool but a suite of software's, each catering to different testing needs of an organization. It has four components
</p>
<p>Selenium Integrated Development Environment (IDE) Shinya Kasatani of Japan created Selenium IDE, a Firefox extension that can automate the browser through a record-and-playback feature. He came up with this idea to further increase the speed in creating test cases. He donated Selenium IDE to the Selenium Project in 2006.
</p>
<p>Selenium Remote Control (RC) Unfortunately; testers using Selenium Core had to install the whole application under test and the web server on their own local computers because of the restrictions imposed by the same origin policy. So another Thought Work's engineer, Paul Hammat, decided to create a server that will act as an HTTP proxy to "trick" the browser into believing that Selenium Core and the web application being tested come from the same domain. This system became known as the Selenium Remote Control or Selenium 1
</p>
<p>Web Driver Simon Stewart created Web Driver circa 2006 when browsers and web applications were becoming more powerful and more restrictive with JavaScript programs like Selenium Core. It was the first cross-platform testing framework that could control the browser from the OS level.
</p>
<p>Selenium Grid Selenium Grid was developed by Patrick Light body to address the need of minimizing test execution times as much as possible. He initially called the system "Hosted QA." It was capable of capturing browser screenshots during significant stages, and also of sending out Selenium commands to different machines simultaneously.
</p>
<p>Solution - Oriented Automation TestingApproach [SOAT] using Selenium helps clients overcome these challenges, and speed up the automation of their functional and regression testing
</p>
<p>SOAT - Using Selenium helps enterprises speed up testing using accelerators at the test design layer while keeping the automation suite ?exible to interface with commercial tools, whenever needed. The test framework provides a comprehensive reporting dashboard for managing tests.
