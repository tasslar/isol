<p>Project management is the application of processes, methods, knowledge, skills and experience to achieve the project objectives.
</p>
<p>Projects are separate to business-as-usual activities, requiring people to come together temporarily to focus on specific project objectives. As a result, effective teamwork is central to successful projects.
</p>
<p>Project management is concerned with managing discrete packages of work to achieve objectives. The way the work is managed depends upon a wide variety of factors.
</p>
<p>The scale, significance and complexity of the work are obvious factors: relocating a small office and organizing the Olympics share many basic principles but offer very different managerial challenges.
</p>
